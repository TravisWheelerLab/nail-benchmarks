#! /bin/sh

NAME=benchmark
POS=./$NAME/$NAME.pos

SCRIPT=./scripts/recall.py

OUTPUT_DIR=./output

HMMER_RESULTS=$OUTPUT_DIR/hmmer/results.sorted
HMMER_TARGET_COL=0
HMMER_QUERY_COL=2

NAIL_RESULTS=$OUTPUT_DIR/nail/results.sorted
NAIL_TARGET_COL=0
NAIL_QUERY_COL=1

MMSEQS_RESULTS=$OUTPUT_DIR/mmseqs/results.sorted
MMSEQS_TARGET_COL=0
MMSEQS_QUERY_COL=1

NUM_TRUE_POSITIVES="$(wc -l < $POS)"

echo hmmer: $(python3 $SCRIPT $HMMER_RESULTS $HMMER_TARGET_COL $HMMER_QUERY_COL $NUM_TRUE_POSITIVES)
echo nail: $(python3 $SCRIPT $NAIL_RESULTS $NAIL_TARGET_COL $NAIL_QUERY_COL $NUM_TRUE_POSITIVES)
echo mmseqs: $(python3 $SCRIPT $MMSEQS_RESULTS $MMSEQS_TARGET_COL $MMSEQS_QUERY_COL $NUM_TRUE_POSITIVES)
